//这是学生答案实体
public class StudentAnswer {
   private int studentAnswerNo;
   private int studentNo;
   private int examNo;
   private String answer;
   private int score;
   private int problemNo;
   private int teacherNo;
public int getStudentAnswerNo() {
	return studentAnswerNo;
}
public void setStudentAnswerNo(int studentAnswerNo) {
	this.studentAnswerNo = studentAnswerNo;
}
public int getStudentNo() {
	return studentNo;
}
public void setStudentNo(int studentNo) {
	this.studentNo = studentNo;
}
public int getExamNo() {
	return examNo;
}
public void setExamNo(int examNo) {
	this.examNo = examNo;
}
public String getAnswer() {
	return answer;
}
public void setAnswer(String answer) {
	this.answer = answer;
}
public int getScore() {
	return score;
}
public void setScore(int score) {
	this.score = score;
}
public int getProblemNo() {
	return problemNo;
}
public void setProblemNo(int problemNo) {
	this.problemNo = problemNo;
}
public int getTeacherNo() {
	return teacherNo;
}
public void setTeacherNo(int teacherNo) {
	this.teacherNo = teacherNo;
}
   
}
